#!/usr/bin/env python3

age = 30

if age < 2 : 
    print("you are an infant")
elif age < 4 : 
    print("You are learning how to walk")
elif age < 13 : 
    print("You are a child")
elif age < 20 : 
    print("You are a teenager")
elif age < 65 : 
    print("You are a adult")
elif age >= 65 : 
    print("You are a senior")
