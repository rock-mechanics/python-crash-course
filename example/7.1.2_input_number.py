#!/usr/bin/env python3

age = int(input("What is your age : "))

print("Next year, your age will be {}".format(age + 1))

height = float(input("\nHow tall are you in meters? "))
if height >= 1.2 : 
    print("You are tall enough to ride.")
else : 
    print("You'll be able to ride when you're a little older.")

